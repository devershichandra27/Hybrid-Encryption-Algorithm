void AESEncryption(uchar * plainText, uchar * expandedKey, uchar * cipher)
{
    uchar * state = malloc(16);
    //uchar * expandedKey = malloc(176);
    //expandedKey = keyExpansion(key);
    //key addition for the first round
    for (int i = 0; i < 16; ++i)
    {
     state[i] = plainText[i] ^ expandedKey[i];
    }

    //now the 9 rounds begin
    for(int rounds = 1; rounds<10; rounds++)
    {
        byteSubShiftRow(state);
        mixColumns(state);
        int counter = 0;
        int loc = rounds*16;
        while(counter<16)
        {
            state[counter] ^= expandedKey[loc];
            loc++;
            counter++;
        }
    }

    //10th round
    byteSubShiftRow(state);
    for(int i=0; i<16;i++)
    {
        cipher[i] = state[i] ^ expandedKey[i+160];
    }
    free(state);
}

void AESDecryption(uchar * cipher, uchar * expandedKey, uchar * plainText)
{
    uchar * state = malloc(16);
    //uchar * expandedKey = keyExpansion(key);

    //key whitening
    for (int i = 0; i < 16; ++i)
        state[i] = cipher[i] ^ expandedKey[160+i];

    // 9 rounds of decryption
    for (int rounds = 9; rounds >0 ; rounds--)
    {
        inverseByteSubShiftRow(state);
        int counter = 0;
        int loc = 16*rounds;
        while(counter<16)
        {
            state[counter] ^= expandedKey[loc];
            loc++;
            counter++;
        }
        inverseMixedColumn(state);
    }

    //final 10th round of decryption
    inverseByteSubShiftRow(state);
    for(int i =0; i<16; i++)
        plainText[i] = state[i] ^ expandedKey[i];
    free(state);
}

